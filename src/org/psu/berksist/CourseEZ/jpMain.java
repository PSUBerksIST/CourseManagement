    public void CreateFrame(JPanel jpIn, String strIn) {
        intWindowCounter++;

        JInternalFrame jifTemp = new JInternalFrame("New Frame "
                + intWindowCounter + " - " + strIn, true, true, true, true);

        jifTemp.add(jpIn);
        jifTemp.pack();

        jdpMain.add(jifTemp);
        jifTemp.setVisible(true);

    }
    
    
    
